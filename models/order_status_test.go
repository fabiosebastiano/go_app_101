package models_test

import (
	"testing"

	"github.com/go_apps_101/models"
)

func TestOrderStatus(t *testing.T) {
	if models.ACCEPTED != 0 {
		t.Errorf("unexpected value for ACCEPTED status, got : %v, want: 0", models.ACCEPTED)
	}
	if models.PREPARING != 1 {
		t.Errorf("unexpected value for PREPARING status, got : %v, want: 1", models.PREPARING)
	}
	if models.DELIVERING != 2 {
		t.Errorf("unexpected value for DELIVERING status, got : %v, want: 2", models.DELIVERING)
	}
	if models.COMPLETED != 3 {
		t.Errorf("unexpected value for COMPLETED status, got : %v, want: 3", models.COMPLETED)
	}
	if models.REJECTED != 4 {
		t.Errorf("unexpected value for REJECTED status, got : %v, want: 4", models.REJECTED)
	}
}
